# Integer
_int = 1
print(_int)

# Float
_float = 1.0
print(_float)

# Complex Number
_complex_number = 1 + 1j
print(_complex_number)

# String
_str = "One"
print(_str)

# Boolean
_bool = True
print(_bool)

# None
_none = None
print(_none)

# List
_list = [1, "One", True]
print(_list)

# Tuple
_tuple = (1, 1.11, "One")
print(_tuple)

# Range
_range = range(11)
print(_range)

# Dictionary
_dict = {"One": 1, "Eleven": 11}
print(_dict)

# Set
_set = {1, 11, 111, 1111}
print(_set)

# FrozenSet
_frozen_set = ([1,11,111,1111])
print(_frozen_set)

